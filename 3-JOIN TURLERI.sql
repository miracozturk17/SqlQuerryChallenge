/*
FARKLI TABLOLARDAKI VERILERI BIRLESTIREREK YENI BIR SORGU TABLOSU OLUSTURMAK ICIN TABLOLAR ARASINDA JOIN ISLEMLERI GERCEKLESTIRILIR.
MANTIGI TAMAMEN KUMELERT KURAMINDAN ESINLENILMISTIR.
*/

SELECT * FROM PERSON
SELECT * FROM DEPARTMENT

/*
INNER JOIN ILE ORTAK VERISI ESLESEN KAYITLAR GETIRILIR.
PERSON TABLOSUNDA INNER JOIN ILE ICERISINDE DEPARTMAN ID SI BULUNMAYAN KULLANICILAR GELMEZ.
*/
SELECT * FROM PERSON

SELECT * FROM PERSON P
INNER JOIN DEPARTMENT D ON P.DepartmentID=D.DepartmentID

SELECT * FROM PERSON P
INNER JOIN DEPARTMENT D ON P.DepartmentID=D.DepartmentID
WHERE P.DepartmentID IS NULL  --NULL OLAN DEGERLERI DONDUREN SORGU --NOT NULL OLAMAYN DEGERLERI GETIREN SORGU

/*
SOL TABLO BAZ ALINARAK SAG TABLODAN VERI BIRLESTIRMESI GERCEKLESTIRILIR.
PERSON TABLOSUNDA BULUNAN SIRKET BILGILERI OLMAYAN KULLANICILARIDA NULL OLMASINA RAGMEN GELIR.
*/
SELECT * FROM PERSON P
LEFT JOIN DEPARTMENT D ON P.DepartmentID=D.DepartmentID

SELECT * FROM PERSON P
LEFT JOIN DEPARTMENT D ON P.DepartmentID=D.DepartmentID
WHERE P.DepartmentID IS NULL --NULL OLAN DEGERLERI GETIREN SORGU --NOT NULL OLAMAYN DEGERLERI GETIREN SORGU

SELECT * FROM PERSON P
LEFT OUTER JOIN DEPARTMENT D ON P.DepartmentID=D.DepartmentID

SELECT * FROM PERSON P
LEFT OUTER JOIN DEPARTMENT D ON P.DepartmentID=D.DepartmentID
WHERE P.DepartmentID IS NULL --NULL OLAN DEGERLERI GETIREN SORGU --NOT NULL OLAMAYN DEGERLERI GETIREN SORGU

/*
SAG TABLO BAZ ALINARAK SOL TABLODAN VERI BIRLESTIRMESI GERCEKLESTIRILIR.
DEPARTMAN TABLOSUNDA BULUNAN KISI BILGILERI OLMAYAN KULLANICILARIDA NULL OLMASINA RAGMEN GELIR.
*/
