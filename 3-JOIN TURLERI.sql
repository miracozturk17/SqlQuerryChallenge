RIGHT JOIN DEPARTMENT D ON P.DepartmentID=D.DepartmentID

SELECT * FROM PERSON P
RIGHT JOIN DEPARTMENT D ON P.DepartmentID=D.DepartmentID
WHERE P.DepartmentID IS NULL --NULL OLAN DEGERLERI GETIREN SORGU --NOT NULL OLAMAYN DEGERLERI GETIREN SORGU

SELECT * FROM PERSON P
RIGHT OUTER JOIN DEPARTMENT D ON P.DepartmentID=D.DepartmentID

SELECT * FROM PERSON P
RIGHT OUTER JOIN DEPARTMENT D ON P.DepartmentID=D.DepartmentID
WHERE P.DepartmentID IS NULL --NULL OLAN DEGERLERI GETIREN SORGU --NOT NULL OLAMAYN DEGERLERI GETIREN SORGU

/*
IKI TABLOYU DOGRUDAN BIRLESTIREN SORGU.
DEPARTMAN VE KULLANICI TABLOSUNDA TEKLI BAZDA FARKLI OLAN TUM VERILERI ALIR.
*/
