/*
IKI TABLOYU CAPRAZLAMA OLARAK BIRLESTIREN SORGU.
DEPARTMAN TABOSUNDAKI VERILERI KULLANICILARLA TEKRARLI OLARAK CAPRAZLAYARAK BIRLESTIRIR.
